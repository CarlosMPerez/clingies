CREATE TABLE IF NOT EXISTS styles (
    [id] TEXT PRIMARY KEY,
    [body_color] TEXT,
    [title_color] TEXT,
    [body_font] TEXT,
    [body_font_color] TEXT,
    [body_font_size] INTEGER,
    [body_font_decorations] TEXT,
    [title_font] TEXT,
    [title_font_size] INTEGER,
    [title_font_color] TEXT,
    [title_font_decorations] TEXT
);